CREATE TABLE "HR".SUBJECT 
(	"강좌이름" VARCHAR2(100), 
	"강의실" VARCHAR2(100), 
	 CONSTRAINT "SUBJECT_PK" PRIMARY KEY ("강좌이름")
)

CREATE TABLE "HR".DEPARTMENT
(	"학과" VARCHAR2(100) PRIMARY KEY, 
	"학과사무실" VARCHAR2(100)
)

CREATE TABLE "HR".STUDENT
(	"학생번호" NUMBER(38,0) PRIMARY KEY, 
	"학생이름" VARCHAR2(100),
	"주소" VARCHAR2(100),
	"학과" VARCHAR2(100),
	CONSTRAINT "FK_STUDENT" FOREIGN KEY ("학과")
	  REFERENCES "HR".DEPARTMENT ("학과")
)

CREATE TABLE "HR".RECORD
(	"학생번호" NUMBER(38,0), 
	"강좌이름" VARCHAR2(100),
	"성적" VARCHAR2(100),
	CONSTRAINT "FK_RECORD1" FOREIGN KEY ("학생번호")
	  REFERENCES "HR".STUDENT ("학생번호"),
	CONSTRAINT "FK_RECORD2" FOREIGN KEY ("강좌이름")
	  REFERENCES "HR".SUBJECT ("강좌이름")
)

