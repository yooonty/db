CREATE TABLE "HR".VIDEO 
(	"제목" VARCHAR2(100), 
	"채널명" VARCHAR2(100),
	"조회수" NUMBER(38,0),
	"카테고리" VARCHAR2(100),
	 CONSTRAINT "VIDEO_PK" PRIMARY KEY ("제목"),
	 CONSTRAINT "VIDEO_FK" FOREIGN KEY ("채널명")
	  REFERENCES "HR".CHANNEL ("채널명")
)

CREATE TABLE "HR".CHANNEL
(	"채널명" VARCHAR2(100), 
	"구독자수" NUMBER(38,0),
	"동영상수" NUMBER(38,0),
	 CONSTRAINT "CHANNEL_PK" PRIMARY KEY ("채널명")
)

CREATE TABLE "HR". YOUTUBE_MEMBER
(	"이름" VARCHAR2(100), 
	"이메일" VARCHAR2(100),
	"가입일" DATE,
	"구독채널" VARCHAR2(100),
	 CONSTRAINT "YOUTUBE_MEMBER_PK" PRIMARY KEY ("이메일"),
	 CONSTRAINT "YOUTUBE_MEMBER_FK" FOREIGN KEY ("구독채널")
	  REFERENCES "HR".CHANNEL ("채널명")
)

